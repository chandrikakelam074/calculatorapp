package p1mvn_con.cal;

public class calculator {

	public int add(int num1, int num2) {
		int sum = num1 + num2;
		return sum;

	}
	
	
	public int substract(int num1, int num2) {
		int sub = num1 - num2;
		return sub;

	}
	
	public int multiply(int num1, int num2) {
		int mup = num1 * num2;
		return mup;

	}
	
	public int divide(int num1, int num2) {
		int div = num1 / num2;
		return div;

	}
}
